
/**
 * @enum this enum describes modal themes
 * @property info should be used for shows information
 * @property warning should be used to alert you to something that requires attention
 * @property danger should be used to alert you to something important that may be non-reversible
 */
export enum ModalTheme {
  INFO = 'info',
  WARNING = 'warning',
  DANGER = 'danger'
}
