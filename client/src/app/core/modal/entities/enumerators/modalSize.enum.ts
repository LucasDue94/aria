
/**
 * @enum this enum describes the modal sizes for screen
 * @property small should be used for a modal with little content
 * @property medium should be used for a modal with considerable content
 * @property larger should be used for a modal with a lot content
 */
export enum ModalSize {
  SMALL = 'small',
  MEDIUM = 'medium',
  LARGER = 'larger'
}
